#!/bin/bash
# Main experiment runner script.
# Usage: bash scripts/run_experiment.sh <phase> [--parallel N]
#
# Phases: phase1_baseline, phase2_lis, phase3_mixture, phase4_lora
# --parallel N: run N experiments simultaneously (default: 1)

set -euo pipefail

# First positional arg is the phase; remaining args are flags
PHASE="${1:-}"
shift || true   # consume the phase arg; remaining are flags like --parallel N

PARALLEL=1
LOG_DIR="logs"
TRAIN_LANGS=""   # empty = use phase default

while [[ $# -gt 0 ]]; do
  case "$1" in
    --parallel) PARALLEL="$2"; shift 2 ;;
    --train_langs) TRAIN_LANGS="$2"; shift 2 ;;
    *) echo "Unknown argument: $1"; exit 1 ;;
  esac
done

# Auto-detect GPU and generate accelerate configs if needed
GPU_COUNT=$(nvidia-smi --list-gpus 2>/dev/null | wc -l || echo 0)
if [[ ! -f configs/accelerate_fullgpu.yaml ]] || [[ $GPU_COUNT -gt 0 ]]; then
  bash scripts/setup_accelerate.sh
fi

# Use dynamic configs (generated by setup_accelerate.sh)
ACCEL_FULL="configs/accelerate_fullgpu.yaml"
ACCEL_HALF="configs/accelerate_halfgpu.yaml"
ACCEL_LORA="configs/accelerate_loragpu.yaml"

# Fallback to legacy names if auto-gen failed
[[ -f "$ACCEL_FULL" ]] || ACCEL_FULL="configs/accelerate_4gpu.yaml"
[[ -f "$ACCEL_LORA" ]] || ACCEL_LORA="configs/accelerate_2gpu.yaml"

mkdir -p "$LOG_DIR"

MODELS=("/root/project/models/Qwen3.5-9B-Base")
LANGUAGES=("en" "fr" "zh" "sw" "th" "bn" "yo")

run_with_limit() {
  local max_jobs=$1
  shift
  local cmd=("$@")
  while (( $(jobs -r | wc -l) >= max_jobs )); do
    sleep 5
  done
  "${cmd[@]}" &
}

case $PHASE in
  phase1_baseline)
    echo "=== Phase 1: Baseline Evaluation ==="
    for MODEL in "${MODELS[@]}"; do
      MODEL_SHORT=$(basename "$MODEL")
      echo "Evaluating baseline: $MODEL_SHORT"
      python scripts/evaluate.py \
        --model_path "$MODEL" \
        --tasks all \
        --languages "fr,zh,sw,th,bn,yo" \
        --output "results/phase1_baseline/${MODEL_SHORT}_baseline.json" \
        2>&1 | tee "$LOG_DIR/phase1_${MODEL_SHORT}.log"
    done
    echo "Phase 1 complete."
    ;;

  phase2_lis)
    echo "=== Phase 2: LIS Matrix Experiments ==="
    # Allow partial runs via --train_langs "en fr zh"
    if [[ -n "$TRAIN_LANGS" ]]; then
      IFS=' ' read -ra PHASE2_LANGS <<< "$TRAIN_LANGS"
    else
      PHASE2_LANGS=("${LANGUAGES[@]}")
    fi
    for MODEL in "${MODELS[@]}"; do
      MODEL_SHORT=$(basename "$MODEL")
      for LANG in "${PHASE2_LANGS[@]}"; do
        EXP_NAME="lis_${MODEL_SHORT}_train_${LANG}"
        echo "Starting: $EXP_NAME"

        run_with_limit "$PARALLEL" bash -c "
          set -e
          accelerate launch \
            --config_file $ACCEL_FULL \
            scripts/train.py \
            --model $MODEL \
            --train_lang $LANG \
            --train_samples 500 \
            --method standard_lora \
            --output_dir results/phase2_lis_matrix/$EXP_NAME \
            --config configs/experiments/lis_matrix.yaml \
            --no_wandb \
            2>&1 | tee $LOG_DIR/${EXP_NAME}_train.log

          python scripts/evaluate.py \
            --model_path results/phase2_lis_matrix/$EXP_NAME \
            --tasks all \
            --languages fr,zh,sw,th,bn,yo \
            --skip_flores \
            --output results/phase2_lis_matrix/${EXP_NAME}_eval.json \
            2>&1 | tee $LOG_DIR/${EXP_NAME}_eval.log
        "
      done
    done
    wait
    echo "Phase 2 training done. Computing LIS matrix..."
    python scripts/compute_lis.py \
      --results_dir results/phase2_lis_matrix \
      --baseline_dir results/phase1_baseline \
      --output_dir results/phase2_lis_matrix
    echo "Phase 2 complete."
    ;;

  phase3_mixture)
    echo "=== Phase 3: Data Mixture Experiments ==="
    RATIOS=(0.1 0.2 0.3 0.5 0.7 0.9)
    LANGS=("sw" "zh")

    for MODEL in "${MODELS[@]}"; do
      MODEL_SHORT=$(basename "$MODEL")
      for LANG in "${LANGS[@]}"; do
        for RATIO in "${RATIOS[@]}"; do
          EXP_NAME="mixture_${MODEL_SHORT}_${LANG}_en${RATIO}"
          echo "Starting: $EXP_NAME"

          run_with_limit "$PARALLEL" bash -c "
            accelerate launch \
              --config_file $ACCEL_LORA \
              scripts/train.py \
              --model $MODEL \
              --train_lang $LANG \
              --english_ratio $RATIO \
              --total_samples 4000 \
              --method standard_lora \
              --output_dir results/phase3_data_mixture/$EXP_NAME \
              --config configs/experiments/mixture.yaml \
              2>&1 | tee $LOG_DIR/${EXP_NAME}_train.log

            python scripts/evaluate.py \
              --model_path results/phase3_data_mixture/$EXP_NAME \
              --tasks english_main,target_lang \
              --target_lang $LANG \
              --include_mt_bench \
              --output results/phase3_data_mixture/${EXP_NAME}_eval.json \
              2>&1 | tee $LOG_DIR/${EXP_NAME}_eval.log
          "
        done
      done
    done
    wait
    echo "Phase 3 complete. Run: python analysis/plot_pareto_curve.py to analyze."
    ;;

  phase4_lora)
    echo "=== Phase 4: LoRA Comparison Experiments ==="
    METHODS=("full_ft" "standard_lora" "mixed_lora" "isolated_lora")
    LANGS=("sw" "zh")

    for MODEL in "${MODELS[@]}"; do
      MODEL_SHORT=$(basename "$MODEL")
      for METHOD in "${METHODS[@]}"; do
        for LANG in "${LANGS[@]}"; do
          EXP_NAME="lora_${MODEL_SHORT}_${METHOD}_${LANG}"
          echo "Starting: $EXP_NAME"

          # Mixed-LoRA needs Phase 3 optimal ratio — set it here after Phase 3 analysis
          OPTIMAL_RATIO_SW=0.3  # update after Phase 3
          OPTIMAL_RATIO_ZH=0.2  # update after Phase 3

          EXTRA_ARGS=""
          if [[ "$METHOD" == "mixed_lora" ]]; then
            if [[ "$LANG" == "sw" ]]; then
              EXTRA_ARGS="--english_ratio $OPTIMAL_RATIO_SW --total_samples 4000"
            elif [[ "$LANG" == "zh" ]]; then
              EXTRA_ARGS="--english_ratio $OPTIMAL_RATIO_ZH --total_samples 4000"
            fi
          fi

          ACCEL_CFG="$ACCEL_FULL"
          if [[ "$METHOD" != "full_ft" ]]; then
            ACCEL_CFG="$ACCEL_LORA"
          fi

          run_with_limit "$PARALLEL" bash -c "
            accelerate launch \
              --config_file $ACCEL_CFG \
              scripts/train.py \
              --model $MODEL \
              --train_lang $LANG \
              --train_samples 500 \
              --method $METHOD \
              $EXTRA_ARGS \
              --output_dir results/phase4_lora_isolation/$EXP_NAME \
              --config configs/experiments/lora_comparison.yaml \
              2>&1 | tee $LOG_DIR/${EXP_NAME}_train.log

            python scripts/evaluate.py \
              --model_path results/phase4_lora_isolation/$EXP_NAME \
              --tasks all \
              --target_lang $LANG \
              --include_mt_bench \
              --include_lcb \
              --output results/phase4_lora_isolation/${EXP_NAME}_eval.json \
              2>&1 | tee $LOG_DIR/${EXP_NAME}_eval.log
          "
        done
      done
    done
    wait
    echo "Phase 4 complete."
    ;;

  *)
    echo "Usage: bash scripts/run_experiment.sh <phase> [--parallel N]"
    echo "Phases: phase1_baseline, phase2_lis, phase3_mixture, phase4_lora"
    exit 1
    ;;
esac
